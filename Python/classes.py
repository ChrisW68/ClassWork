
#Global Variable
x = 5
y = 6

class calc:
	#Addition of the x and y global variable
	def add(x,y):
		answer = x+y
		print(answer)

	def sub(x,y):
		answer = x-y
		print(answer)

	def mult(x,y):
		answer = x * y
		print(answer)

	def div(x,y):
		answer = x / y
		print(answer)

calc.div (x,y)

calc.mult(x,y)

calc.add(x,y)

calc.sub(x,y)